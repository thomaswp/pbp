<template>
<div class="search">
  <input v-model="value">
</div>
</template>

<script>
export default {
  props: ['start-value', 'search'],
  data() {
    return {
      value: "",
    };
  },
  watch: {
    value() {
      this.$emit('search', this.value);
    }
  }
}
</script>


